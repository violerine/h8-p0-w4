function countProfit(shoppers) {
    let listBarang = [ ['Sepatu Stacattu', 1500000, 10],
                       ['Baju Zoro', 500000, 2],
                       ['Sweater Uniklooh', 175000, 1]
                     ];
var arrayKosong=[]
if(shoppers.length==0){
    return arrayKosong
}
  var array=[]
  for(var i=0; i<listBarang.length; i++){
        array.push({Product:listBarang[i][0], Shoppers:[], Leftover:listBarang[i][2], Totalprofit:0})
  }

  for(var j=0; j<array.length; j++){
      for(var k=0; k<shoppers.length; k++){
        if(shoppers[k].product==array[j].Product && array[j].Leftover-shoppers[k].amount>=0 ){
            array[j].Shoppers.push(shoppers[k].name)
            array[j].Leftover-=shoppers[k].amount
            }
        }
    }
    for(var i=0; i<listBarang.length; i++){
        if(listBarang[i][2]-array[i].Leftover==0){
            array[i].Totalprofit=0
        }
        var kurang=listBarang[i][2]-array[i].Leftover
        var hasil=listBarang[i][1]*kurang
        array[i].Totalprofit=hasil
    }

return array

}
